class Gallery extends Component {
  state = {
    selectedImgId: imagesList[0].id,
  }

  onClickChangeImg = imgId => {
    this.setState({selectedImgId: imgId})
  }

  render() {
    const {selectedImgId} = this.state

    const selectedImgDetails = imagesList.filter(
      eachImg => eachImg.id === selectedImgId,
    )

    const selectedImgUrl = selectedImgDetails[0].imageUrl
    const selectedImgAltText = selectedImgDetails[0].imageAltText

    console.log(selectedImgUrl)
    console.log(selectedImgAltText)
    const mainEle = (
      <div className="mainBgContainer">
        <div className="galleryBgContainer">
          <img
            alt={selectedImgAltText}
            src={selectedImgUrl}
            className="galleryImg"
          />
          <h1 className="headerText"> Nature Photography</h1>
          <p className="description">Nature Photography by Rahul</p>

          <ul className="thumbnailContainer">
            {imagesList.map(eachImgDetails => (
              <ThumbnailItem
                key={eachImgDetails.id}
                galleryDetails={eachImgDetails}
                selectedImgId={selectedImgId}
                onClickChangeImg={this.onClickChangeImg}
              />
            ))}
          </ul>
        </div>
      </div>
    )

    return mainEle
  }
}

export default Gallery
